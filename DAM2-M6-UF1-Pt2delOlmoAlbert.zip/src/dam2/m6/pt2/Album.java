package dam2.m6.pt2;

public class Album {
	private int year;
	private String albumName;
	
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public String getSongName() {
		return albumName;
	}
	public void setSongName(String songName) {
		this.albumName = songName;
	}
	
}
